/* 기타 필요한 함수 추가 선언 및 정의 */

void print_block(BYTE *block, char *keyword, int s){
    int i = 0;
    printf(" - %s : ", keyword);
    for(i = 0; i < s; i++) {
        if(i % 16 == 0) printf("\n");
        printf("%02x ", block[i]);
    }
    printf("\n\n");
}

#define getSBoxValue(num) (Sbox[(num)])
#define getSBoxInvert(num) (rSbox[(num)])

BYTE* rotWord(BYTE *w){
    BYTE rotW;
    
    rotW = *(w + 0);
    *(w + 0) = *(w + 1);
    *(w + 1) = *(w + 2);
    *(w + 2) = *(w + 3);
    *(w + 3) = rotW;

    return w;
}

void subWord(BYTE *x){
    int idx;
    
    for(idx = 0; idx < WORD_SIZE; idx++)
        *(x + idx) = getSBoxValue(*(x + idx));
}

void XORWord(BYTE *w0, BYTE *w3, BYTE *g){
    int idx;

    for(idx = 0; idx < WORD_SIZE; idx++) {
        *(w0 + idx) = *(w3 + idx) ^ *(g + idx);
    }
}

/*  <키스케줄링 함수>
 *   
 *  key         키스케줄링을 수행할 16바이트 키
 *  roundKey    키스케줄링의 결과인 176바이트 라운드키가 담길 공간
 */
void expandKey(BYTE *key, BYTE *roundKey){
    /* 추가 구현 */
    BYTE w[WORD_SIZE];
    int idx;

    // The first round key is the key itself.
    memcpy(roundKey, key, sizeof(BYTE) * KEY_SIZE);

    for(idx = BLOCK_SIZE; idx < ROUNDKEY_SIZE; idx+=WORD_SIZE){
        // All other round keys are found from the previous round keys.
        memcpy(w, roundKey + idx - WORD_SIZE, WORD_SIZE); // w3

        if(idx % BLOCK_SIZE == 0){
            subWord(rotWord(w));
            w[0] ^= RCon[idx/BLOCK_SIZE]; // g
        }
        XORWord(roundKey + idx, roundKey + idx - BLOCK_SIZE, w); // w0 xor g
    }

}


/*  <SubBytes 함수>
 *   
 *  block   SubBytes 수행할 16바이트 블록. 수행 결과는 해당 배열에 바로 반영
 *  mode    SubBytes 수행 모드
 */
 BYTE* subBytes(BYTE *block, int mode){
    /* 필요하다 생각하면 추가 선언 */
    int idx;
    switch(mode){
        case ENC:
            /* 추가 구현 */
            for(idx = 0; idx < BLOCK_SIZE; idx++)
                *(block + idx) = getSBoxValue(*(block + idx));
            break;

        case DEC:
            /* 추가 구현 */
            for(idx = 0; idx < BLOCK_SIZE; idx++)
                *(block + idx) = getSBoxInvert(*(block + idx));
            break;

        default:
            fprintf(stderr, "Invalid mode!\n");
            exit(1);
    }
    
    return block;
}

// shift element of row
void rowShift(BYTE *block, int row){
    BYTE tmpBlock[WORD_SIZE];
    int idx, shiftIdx = 0;
    int start = row * WORD_SIZE + row;

    for(idx = 0; idx < WORD_SIZE; idx++){
        tmpBlock[idx] = *(block + start + shiftIdx);
        shiftIdx += NUM_WORD;
        if(start + shiftIdx >= BLOCK_SIZE){
            start = row;
            shiftIdx = 0;
        }
    }
    
    start = row;
    shiftIdx = 0;
    for(idx = 0; idx < WORD_SIZE; idx++){
        *(block + start + shiftIdx) = tmpBlock[idx];
        shiftIdx += NUM_WORD;
    }

}

/*  <ShiftRows 함수>
 *   
 *  block   ShiftRows 수행할 16바이트 블록. 수행 결과는 해당 배열에 바로 반영
 *  mode    ShiftRows 수행 모드
 */
BYTE* shiftRows(BYTE *block, int mode){
    /* 필요하다 생각하면 추가 선언 */   
    int idx;

    switch(mode){

        case ENC:
            /* 추가 구현 */
            for(idx = 0; idx < NUM_WORD; idx++)
                rowShift(block, idx);
            break;

        case DEC:
            /* 추가 구현 */
            for(idx = 0; idx < NUM_WORD; idx++){
                rowShift(block, idx);
                if(idx == 1 || idx == 3){
                    rowShift(block, idx);
                    rowShift(block, idx);
                }
            }
            break;

        default:
            fprintf(stderr, "Invalid mode!\n");
            exit(1);
    }
    
    return block;
}

/* xtime : multiply by 2 (x2) */
#define xtime(x) ((x<<1) ^ (((x>>7) & 1) * 0x1b)) // 0x1b : x^8+x^4+x^3+x+1 (GF(2^8))
/* multiply two elements */
#define Multiply(x, y)                                  \
        (  ((y & 1) * x) ^                              \
        ((y>>1 & 1) * xtime(x)) ^                       \
        ((y>>2 & 1) * xtime(xtime(x))) ^                \
        ((y>>3 & 1) * xtime(xtime(xtime(x)))) ^         \
        ((y>>4 & 1) * xtime(xtime(xtime(xtime(x))))))

BYTE unitDot(BYTE b, BYTE a){
    if(b == 2)
        return xtime(a); // x2
    else if(b == 3)
        return xtime(a) ^ a; // x2 +1
    else if(b >= 9)
        return Multiply(a, b);
    return a; // x1
}

void matrixDot(BYTE *block, BYTE matrix[4][4]){
    BYTE C[BLOCK_SIZE];
    BYTE A[WORD_SIZE][NUM_WORD];
    int i, j, idx;

    memset(C, 0, sizeof(BYTE) * BLOCK_SIZE);
    memcpy(A, matrix, sizeof(BYTE) * BLOCK_SIZE);

    for(i = 0; i < WORD_SIZE; i++){
        for(j = 0; j < WORD_SIZE; j++){
            // Cij = Aij * Bji
            C[i + j * WORD_SIZE] = unitDot(A[i][0], *(block + j * NUM_WORD + 0)) \
                                ^ unitDot(A[i][1], *(block + j * NUM_WORD + 1)) \
                                ^ unitDot(A[i][2], *(block + j * NUM_WORD + 2)) \
                                ^ unitDot(A[i][3], *(block + j * NUM_WORD + 3));
        }
    }

    for(idx = 0; idx < BLOCK_SIZE; idx++)
        *(block + idx) = C[idx];
}

/*  <MixColumns 함수>
 *   
 *  block   MixColumns을 수행할 16바이트 블록. 수행 결과는 해당 배열에 바로 반영
 *  mode    MixColumns의 수행 모드
 */
BYTE* mixColumns(BYTE *block, int mode){    
    /* 필요하다 생각하면 추가 선언 */   

    switch(mode){
        case ENC:
            /* 추가 구현 */
            matrixDot(block, MCMatrix);
            break;

        case DEC:
            /* 추가 구현 */
            // problem here!!
            matrixDot(block, MCInvMatrix);
            break;

        default:
            fprintf(stderr, "Invalid mode!\n");
            exit(1);
    }
    
    return block;
}

/*  <AddRoundKey 함수>
 *   
 *  block   AddRoundKey를 수행할 16바이트 블록. 수행 결과는 해당 배열에 반영
 *  rKey    AddRoundKey를 수행할 16바이트 라운드키
 */
BYTE* addRoundKey(BYTE *block, BYTE *rKey){
    /* 추가 구현 */
    int idx;

    for(idx = 0; idx < BLOCK_SIZE; idx++)
        *(block + idx) ^= *(rKey + idx);

    return block;
}

/*  <128비트 AES 암복호화 함수>
 *  
 *  mode가 ENC일 경우 평문을 암호화하고, DEC일 경우 암호문을 복호화하는 함수
 *
 *  [ENC 모드]
 *  input   평문 바이트 배열
 *  result  결과(암호문)이 담길 바이트 배열. 호출하는 사용자가 사전에 메모리를 할당하여 파라미터로 넘어옴
 *  key     128비트 암호키 (16바이트)
 *
 *  [DEC 모드]
 *  input   암호문 바이트 배열
 *  result  결과(평문)가 담길 바이트 배열. 호출하는 사용자가 사전에 메모리를 할당하여 파라미터로 넘어옴
 *  key     128비트 암호키 (16바이트)
 */
 
void AES128(BYTE *input, BYTE *result, BYTE *key, int mode){
    int idx;
    BYTE roundKey[ROUNDKEY_SIZE];

    expandKey(key, roundKey);

    if(mode == ENC){
        /* 추가 작업이 필요하다 생각하면 추가 구현 */

        // initial XOR
        addRoundKey(input, roundKey); // w[0,3]

        for(idx = 1; idx < 10; idx++) {
            subBytes(input, mode);
            shiftRows(input, mode);
            mixColumns(input, mode);
            addRoundKey(input, roundKey + (idx * BLOCK_SIZE));
        }
        subBytes(input, mode);
        shiftRows(input, mode);
        addRoundKey(input, roundKey + BLOCK_SIZE * 10); // w[40,43]

        memcpy(result, input, sizeof(BYTE) * BLOCK_SIZE);
    }else if(mode == DEC){
        /* 추가 작업이 필요하다 생각하면 추가 구현 */    

        // initial XOR
        addRoundKey(input, roundKey + BLOCK_SIZE * 10); // w[40,43]

        for(idx = 1; idx < 10; idx++) {
            subBytes(input, mode);
            shiftRows(input, mode);
            // distribution
            mixColumns(input, mode); // MP
            mixColumns(roundKey + ((10 - idx) * BLOCK_SIZE), mode); // MK // w[36,39]
            addRoundKey(input, roundKey + ((10 - idx) * BLOCK_SIZE)); // M(P+K)
        }
        subBytes(input, mode);
        shiftRows(input, mode);
        addRoundKey(input, roundKey); // w[0,3]

        memcpy(result, input, sizeof(BYTE) * BLOCK_SIZE);

    }else{
        fprintf(stderr, "Invalid mode!\n");
        exit(1);
    }
}
